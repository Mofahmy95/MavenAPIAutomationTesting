package POJO;

public class HttpResponseStatusCodes {

    final public int ok = 200;
    final public int created = 201;
    final public int accepted = 202;
    final public int noContent = 204;
    final public int badRequest = 400;
    final public int unauthorized = 401;
    final public int paymentRequired = 402;
    final public int forbidden = 403;
    final public int notFound = 404;
    final public int payloadTooLarge = 413;
    final public int uriTooLong = 414;
    final public int internalServerError = 500;
    final public int badGateway = 502;
    final public int serviceUnavailable = 503;
    final public int gatewayTimeout = 504;

}
